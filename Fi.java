// *  final keyword can use with a variable, method and class
public class Fi {
  // * when u create a variable with a final keyword it mean it value is constant and cannot be change

  // * when you make a method final it means the method cannot be override

  // * when you make a class final it mean it cannot be extended by another class.
  public static void main(String[] args) {
    final int a = 9;
    a = 7;


  }
}
