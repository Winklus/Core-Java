public class Wrap {
  public static void main(String[] args) {
    Integer n1 = 3;
    System.out.println(n1);

    int n2 = n1.intValue();
    System.out.println(n2);

    String age = "9";

    int n3 = Integer.parseInt(age);
    System.out.println(n3);
    String x = Integer.toString(7);
    System.out.println(x);

  }
}
